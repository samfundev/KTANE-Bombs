<h1 class="header">Season Info</h1>

<div class="block">
	Participation in seasons is optional. If you intend to only submit your solves to the global leaderboard you can
	ignore the information in this page.
</div>

<div class="block">
	<h2>What are seasons?</h2>

	<ul>
		<li>
			Seasons are timed events that will last for approximately 3 months each. During each season, players will compete
			for who can solve the most bombs that fit certain criteria explained in this page.
		</li>
		<li>
			The leaderboard for the season is separate from the global leaderboard, and is available through the "Seasons"
			button at the top of the page.
		</li>
		<li>
			The season leaderboards are not shared between seasons and players can only add solves to the leaderboard of a
			season during the 3 months that season is active.
		</li>
		<li>
			If you want a solve to be considered for the ongoing season, you need to activate the respective checkmark during
			the upload of the solve.
		</li>
		<li>
			At the end of each season, the top 3 players in each of these categories will get a reward: Total distinct solves,
			Team distinct solves and EFM distinct solves. The rewards will last during the following season and are:
		</li>
		<ul>
			<li>1st of each category: Colored name on this site and a colored role on the official KTANE server.</li>
			<li>2nd of each category: Colored name on this site</li>
			<li>3rd of each category: Colored name on this site</li>
		</ul>
	</ul>
</div>
<div class="block">
	<h2>Permanent Rules</h2>

	<p>These rules need to be followed for any season solve</p>
	<ul>
		<li>Every rule present in the rules page also applies to the seasons.</li>
		<li>
			Each player participating in a solve must record their screen and/or physical tools and submit it as evidence
			during solve upload. Different roles require different screens recorded:
		</li>
		<ul>
			<li>Defuser: must record the bomb and the audio communication with the experts.</li>
			<li>
				Experts: must record any tools they use (this includes manuals, notepads, calculators, drawing tools and
				others).
			</li>
			<li>EFM: must record the bomb and any tools they use.</li>
		</ul>

		<li>
			Season solves must be of bombs submitted to this website within the 3 months prior to the current season's
			beginning date. Each season can have exceptions to this rule
		</li>
	</ul>
</div>

<div class="block">
	<h2><a href="/season/Back%20to%20Basics">Season 2</a> Rules</h2>

	<p>These rules need to be followed for season 2 solves</p>
	<ul>
		<li>
			Experts and EFM can only use notepads with the same functionalities as those enabled by default in Notepad++
			without accessing menus or keybinds (with the exception of dark mode, find, copy, and paste), manuals and
			calculators with the same functionalities as the
			<a href="https://samfundev.github.io/TextFieldCalculator/Calculator-v2.html?locked">
				<b>Restricted</b> Text Field Calculator
			</a>. Any other tool is not allowed.
		</li>
		<li>
			The only manuals allowed to be used are the <b>default manuals</b> of each module on the mission being played, and those
			that they directly link to.
		</li>
		<li>Modded widgets are not allowed.</li>
		<li>
			Note taking systems and the clipboard need to start empty. Manuals need to start in their default state. This
			applies at the beginning of each bomb, including sequence bombs.
		</li>
		<li>Each team solve needs to have a maximum of 1 defuser and 2 experts.</li>
		<li>
			The following bombs are also allowed for season 2 solves:
			<ul>
				<li><a href="/mission/Easy%20Bomb">Easy Bomb</a></li>
				<li><a href="/mission/Swan%20Hell">Swan Hell</a></li>
			</ul>
		</li>
	</ul>
</div>

<div class="block">
	<h2>FAQ</h2>

	<ul>
		<li>
			I submitted a solve to the site before the season started, but it fits the criteria required for the current
			season. Can it be used as a season solve?
			<ul>
				<li>No, you would need to solve it again.</li>
			</ul>
		</li>

		<li>
			Are base converters/caesar cipher decryptors/macros allowed for season 2 solves?
			<ul>
				<li>No.</li>
			</ul>
		</li>

		<li>
			For technical reasons, one of the players can't fit their own POV in a single video. Can they submit more than 1
			video as evidence?

			<ul>
				<li>Yes.</li>
			</ul>
		</li>

		<li>
			Are optimized, interactive, and other custom manuals allowed for season 2?
			<ul>
				<li>No.</li>
			</ul>
		</li>

		<li>
			Can tools (scissors, glue, etc.) be used to create physical items to help solve modules?
			<ul>
				<li>
					Yes, as long as these items are created during the bomb, starting from the allowed objects (default manuals
					and empty notepads).
				</li>
			</ul>
		</li>
	</ul>
</div>

